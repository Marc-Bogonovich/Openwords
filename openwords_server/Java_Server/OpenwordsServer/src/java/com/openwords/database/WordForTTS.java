package com.openwords.database;

public class WordForTTS {

    private int wordId;
    private String word;

    public WordForTTS() {
    }

    public int getWordId() {
        return wordId;
    }

    public void setWordId(int wordId) {
        this.wordId = wordId;
    }

    public String getWord() {
        return word;
    }

    public void setWord(String word) {
        this.word = word;
    }

}

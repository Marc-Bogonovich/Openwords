package com.openwords.database;

public class WordForTTS {

    private long wordId;
    private String word;

    public WordForTTS() {
    }

    public long getWordId() {
        return wordId;
    }

    public void setWordId(long wordId) {
        this.wordId = wordId;
    }

    public String getWord() {
        return word;
    }

    public void setWord(String word) {
        this.word = word;
    }

}
